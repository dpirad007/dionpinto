    import math
    class Solution:
        def maxProfit(self, prices: List[int]) -> int:
            max_profit=0
            mi=math.inf
            for i in range(len(prices)):
                if(prices[i]<mi):mi=prices[i]
                max_profit=max(abs(prices[i]-mi),max_profit)
            return max_profit
				`}			
			</code></pre>
